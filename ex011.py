# altura e largura da parede

al_parede = float(input("Qual é a altura da parede?: "))
lar_parede = float(input("Qual é a largura da parede?: "))
area = (al_parede * lar_parede)
print(" a area dessa parede é {}".format(area))
tinta = area / 2

print("você ira precisar de {} tinta para pintar toda a parede.".format(tinta))

